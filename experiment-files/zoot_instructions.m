function zoot_instructions(s, window, devNum, white)


if s.exptStage == 0

    DrawFormattedText(window, ...
        WrapString(['In this task you will see up to two horizontal or vertical gratings ' ...
        'flash on the screen in a sequence. There will be a tone before the gratings that tells you which grating to attend to ' ...
        'and another tone after the gratings that tells you which grating''s orientation to report. ' ...
        'A high tone refers to T1, a low tone refers to T2, and a combinatory tone refers to both stimuli for both the first and second tones. ' ...
        'For now, you will be asked to pay attention to both stimuli on each trial. ' ...
        'Your task is to report if the grating cued by the second tone ' ...
        'is tilted  clockwise or counterclockwise from the horizontal ' ...
        'or vertical axes. ' ...
        'This will be a slowed down practice version of the experiment to get familiar with the task. Take your time and do your best! ' ...
        '\n Press "1" to report counter-clockwise ' ...
        '\n Press "2" to report clockwise '  ...
        '\n Press "3" to report absent. ']) ...
        ,'centerblock','center',[1 1 1]*white);

elseif s.exptStage == 1
    DrawFormattedText(window, ...
        WrapString(['In this task you will see up to two horizontal or vertical gratings ' ...
        'flash on the screen in a sequence. There will be a tone before the gratings that tells you which grating to attend to ' ...
        'and another tone after the gratings that tells you which grating''s orientation to report. ' ...
        'A high tone refers to T1, a low tone refers to T2, and a combinatory tone refers to both stimuli for both the first and second tones. ' ...
        'For now, you will be asked to pay attention to both stimuli on each trial. ' ...
        'Your task is to report if the grating cued by the second tone ' ...
        'is tilted  clockwise or counterclockwise from the horizontal ' ...
        'or vertical axes. ' ...
        'This will be a real time practice version of the experiment to get familiar with the task. Good luck and do your best! ' ...
        '\n Press "1" to report counter-clockwise ' ...
        '\n Press "2" to report clockwise '  ...
        '\n Press "3" to report absent. ']) ...
        ,'centerblock','center',[1 1 1]*white);


  elseif s.exptStage == 2
    DrawFormattedText(window, ...
        WrapString(['In this task you will see up to two horizontal or vertical gratings ' ...
        'flash on the screen in a sequence. There will be a tone before the gratings that tells you which grating to attend to ' ...
        'and another tone after the gratings that tells you which grating''s orientation to report. ' ...
        'A high tone refers to T1, a low tone refers to T2, and a combinatory tone refers to both stimuli for both the first and second tones. ' ...
        'For now, you will be asked to pay attention to both stimuli on each trial. ' ...
        'Your task is to report if the grating cued by the second tone ' ...
        'is tilted  clockwise or counterclockwise from the horizontal ' ...
        'or vertical axes. ' ...
        'This will be the thresholding step of the experiment to get your perceptual threshold for tilt. It is meant to get a little difficult so ' ...
        'do not worry if it feels hard! Good luck! ' ...
        '\n Press "1" to report counter-clockwise ' ...
        '\n Press "2" to report clockwise '  ...
        '\n Press "3" to report absent. ']) ...
        ,'centerblock','center',[1 1 1]*white);


  elseif s.exptStage == 3
    DrawFormattedText(window, ...
        WrapString(['In this task you will see up to two horizontal or vertical gratings ' ...
        'flash on the screen in a sequence. There will be a tone before the gratings that tells you which grating to attend to ' ...
        'and another tone after the gratings that tells you which grating''s orientation to report. ' ...
        'A high tone refers to T1, a low tone refers to T2, and a combinatory tone refers to both stimuli for both the first and second tones. ' ...
        'For now, the first tone and the second tone will always match and '....
        'you will be asked to pay attention to either the first or the second grating. ' ...
        'Your task is to report if the grating cued by the second tone' ...
        'is tilted  clockwise or counterclockwise from the horizontal ' ...
        'or vertical axes. ' ...
        'This will be a practice of the experiment to get familiar with the tones.  Good luck! ' ...
        '\n Press "1" to report counter-clockwise ' ...
        '\n Press "2" to report clockwise '  ...
        '\n Press "3" to report absent. ']) ...
        ,'centerblock','center',[1 1 1]*white);

elseif s.exptStage == 4
    DrawFormattedText(window, ...
        WrapString(['In this task you will see up to two horizontal or vertical gratings ' ...
        'flash on the screen in a sequence. There will be a tone before the gratings that tells you which grating to attend to ' ...
        'and another tone after the gratings that tells you which grating''s orientation to report. ' ...
        'A high tone refers to T1, a low tone refers to T2, and a combinatory tone refers to both stimuli for both the first and second tones. ' ...
        'Now, you will be asked to pay attention to either the first, second, or both gratings. ' ...
        'The first and second tones will match almost all the time and it is important that you use the first tone to guide your attention appropriately .'...
        'Your task is to report if the grating cued by the second tone' ...
        'is tilted  clockwise or counterclockwise from the horizontal ' ...
        'or vertical axes. ' ...
        'This will be a practice of the full experiment to get familiar with the task conditions. Good luck and do your best!'...
        '\n Press "2" to report clockwise '  ...
        '\n Press "3" to report absent. ']) ...
        ,'centerblock','center',[1 1 1]*white);

