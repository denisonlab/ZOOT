function instructions(p,window,white)


if p.type == 0 && p.static==1

    DrawFormattedText(window, ...
       WrapString(['In this task you will be asked to report the tilt of a striped circle ("grating"). ' ... 
        'For now you will see one grating with tilted from either the vertical or horizontal axis in the center of the screen.\n' ...
        'Your task is to respond if it''s tilted counter-clockwise or clockwise from either vertical or horizontal. ' ...
        'You do not need to respond if it is vertical or horizontal, you only need to tell us how it is tilted.' ...
        'Press "1" to report counter-clockwise, and press "2" to report clockwise.']) ...
        ,'centerblock','center',white);


end
if p.type == 0 && p.static==0


    DrawFormattedText(window, ...
        WrapString(['In this version you will do the same task as before, but the grating will flash briefly on the screen during each presentation.\n' ...
        'This is how gratings will appear later in the main task. Try your best to respond to the stimulus tilt orientation.']) ...
        ,'centerblock','center',white);
end

if p.type == 1

    DrawFormattedText(window, ...
        ['Now you are ready to praxtice the task with two gratings! You will hear a neutral tone that\n' ...
        'will cue your attention to both of the gratings. The gratings will flash in quick succession. \n' ...
        'A response cue tone will inform you of which grating''s orientation, either the first or the second, you should report. \n\n' ...
        'A high tone means to report the first grating''s tilt.\n' ...
        'A low tone means to report the second grating''s tilt.\n\n' ...
        'There will also be two clicks to represent when a grating may appear on the screen. \n' ...
        'For now, both gratings will appear. \n' ...
        'Pay attention to both gratings though, since there is exactly a 50/50 chance either will end up being the target.\n\n' ...
        'We will use the eye-tracker now. The experiment will wait for you to fixate on the dot at the center of the screen before starting each trial.\n' ...
        'You must maintain fixation between the first tone and the second tone (we want to be sure you see the gratings!)\n' ...
        'If you blink or look away while the gratings are presented, it will reset to a new trial\n' ...
        'You can blink or rest your eyes any other times during the experiment.'] ...
        ,'centerblock','center',white);
end

if p.type == 2

    DrawFormattedText(window, ...
        WrapString(['We are now going to do staircasing to find your threshold.\n' ...
        'The task is the same as before, but the difficulty will varying with each trial. ' ...
        'Try your best to respond accurately. ' ...
        'At the end of this section, the task will feel challenging but doable (~80% accuracy). ' ...
        'This procedure will take about 15 minutes. You will get rest screens every few minutes.']) ...
        ,'centerblock','center',white);
end

if p.type == 3

    DrawFormattedText(window, ...
        ['You will now practice the neutral task with two, one, or no target trials.\n' ...
        'In other words, sometimes both gratings will be present, sometimes one will be present and the other absent, and sometimes both will be absent.\n' ...
        'Otherwise, the task is the same.\n' ...
        'Respond to the target grating when it is present with the following keys:\n\n' ...
        '1 - I saw the cued target and it was tilted counter-clockwise.\n' ...
        '2 - I saw the cued target and it was tilted clockwise.\n' ...
        '3 - The cued target was not present.'] ...
        ,'centerblock','center',white);
end

if p.type ==4 || p.type==6

    DrawFormattedText(window, ...
        ['You will now practice the full task with active and neutral precues. It is the same as what you practiced earlier, but now you will be cued to attend to either the first, second, or both gratings.\n' ...
        'Make sure to use the precues as they provide information about which grating to respond to.\n' ...
        'You will only be asked to report the orientation of one grating per trial, either the first or second, never both.\n' ...
        'Keyboard responses:\n\n' ...
        '1 - I saw the cued target and it was tilted counter-clockwise.\n' ...
        '2 - I saw the cued target and it was tilted clockwise.\n' ...
        '3 - The cued target was not present.'] ...
        ,'centerblock','center',white);
end

if p.type ==5

    DrawFormattedText(window, ...
        ['This is a warm up for the main task. It is the same as what you practiced in earlier sessions.\n' ...
        'You will hear a neutral tone that will "pre cue" that the gratings are about to appear.\n' ...
        'Two gratings will flash one after the other, with the same timing on every trial. After\n' ...
        'the gratings flash, a "response cue" tone will tell you which grating you should report the tilt for. \n\n' ...
        'A high tone means to report the first grating''s tilt.\n' ...
        'A low tone means to report the second grating''s tilt.\n\n' ...        
        'Remember to also respond with your confidence: responses:\n\n' ...
        '1 - I saw the cued target and it was tilted counter-clockwise.\n' ...
        '2 - I saw the cued target and it was tilted clockwise.\n' ...
        '3 - The cued target was not present.'] ...
        ,'centerblock','center',white);

end